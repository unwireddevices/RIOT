static gpio_t en_pins[UMDK_LMT01_MAX_SENSOR_COUNT] = UMDK_LMT01_SENSOR_EN_PINS;
static lmt01_t sensors[UMDK_LMT01_MAX_SENSOR_COUNT];

static uwnds_cb_t *callback;

static kernel_pid_t timer_pid;

static msg_t timer_msg = {};
static lptimer_t timer;

static bool is_polled = false;

static struct {
	uint8_t publish_period_min;
} lmt01_config;

static void init_sensors(void) {
	int i = 0;

	for (i = 0; i < UMDK_LMT01_MAX_SENSOR_COUNT; i++) {
		lmt01_t *dev = &sensors[i];

		/* Skip disabled */
		if (!en_pins[i])
			continue;

		/* Initialize */
        gpio_t pin_common = 0;
        gpio_init(UMDK_LMT01_INT_PIN, GPIO_IN_PU);
        if (gpio_read(UMDK_LMT01_INT_PIN) == 0) {
            pin_common = UMDK_LMT01_INT_PIN;
        } else {
            pin_common = UMDK_LMT01_INT_PIN2;
        }
        gpio_init(UMDK_LMT01_INT_PIN, GPIO_AIN);
        
		if (lmt01_init(dev, en_pins[i], pin_common) < 0) {
			printf("[umdk-" _UMDK_NAME_ "] Failed to initialize sensor #%d\n", i);
		}
	}
}

static void prepare_result(module_data_t *data) {
	int results = 0;
	int i;

	int16_t res[4] = { 0x7FFF, 0x7FFF, 0x7FFF, 0x7FFF };

	for (i = 0; i < UMDK_LMT01_MAX_SENSOR_COUNT; i++) {
		if (!en_pins[i]) {
			continue;
		}

		int temp;
		int pulses;
		if ((pulses = lmt01_get_temp(&sensors[i], &temp)) > 0) {
            char buf[10];
            int_to_float_str(buf, temp, 1);
			printf("[umdk-" _UMDK_NAME_ "] Measured %d pulses on #%d: %s\n", pulses, i, buf);
			res[i] = temp;
			results++;
		} else {
			continue;
		}

		/* Delay between sensor switching */
        lptimer_sleep(UMDK_LMT01_SWITCHING_DELAY_MS);
	}

    if (data) {
        data->data[0] = _UMDK_MID_;
        memcpy(data->data + 1, (uint8_t *) res, sizeof(res));
        data->length = sizeof(res) + 1;
    }
}

static void *timer_thread(void *arg) {
    (void)arg;

    msg_t msg;
    msg_t msg_queue[4];
    msg_init_queue(msg_queue, 4);

    puts("[umdk-" _UMDK_NAME_ "] Periodic publisher thread started");

    while (1) {
        msg_receive(&msg);

        module_data_t data = {};
        data.as_ack = is_polled;
        is_polled = false;

        prepare_result(&data);

        /* Notify the application */
        callback(&data);

        /* Restart after delay */
        lptimer_set_msg(&timer, 60000 * lmt01_config.publish_period_min, &timer_msg, timer_pid);
    }
    
    return NULL;
}

static void reset_config(void) {
	lmt01_config.publish_period_min = UMDK_LMT01_PUBLISH_PERIOD_MIN;
}

static void init_config(void) {
	reset_config();

	if (!unwds_read_nvram_config(_UMDK_MID_, (uint8_t *) &lmt01_config, sizeof(lmt01_config))) {
		reset_config();
    }
}

static inline void save_config(void) {
	unwds_write_nvram_config(_UMDK_MID_, (uint8_t *) &lmt01_config, sizeof(lmt01_config));
}

static void set_period (int period) {
    lptimer_remove(&timer);
	lmt01_config.publish_period_min = period;

	/* Don't restart timer if new period is zero */
	if (lmt01_config.publish_period_min) {
		lptimer_set_msg(&timer, 60000 * lmt01_config.publish_period_min, &timer_msg, timer_pid);
		printf("[umdk-" _UMDK_NAME_ "] Period set to %d minutes\n", lmt01_config.publish_period_min);
	} else {
		puts("[umdk-" _UMDK_NAME_ "] Timer stopped");
    }
    save_config();
}

int umdk_lmt01_shell_cmd(int argc, char **argv) {
    if (argc == 1) {
        puts (_UMDK_NAME_ " get - get results now");
        puts (_UMDK_NAME_ " send - get and send results now");
        puts (_UMDK_NAME_ " period <N> - set period to N minutes");
        puts (_UMDK_NAME_ " reset - reset settings to default");
        return 0;
    }
    
    char *cmd = argv[1];
	
    if (strcmp(cmd, "get") == 0) {
        prepare_result(NULL);
    }
    
    if (strcmp(cmd, "send") == 0) {
		/* Send signal to publisher thread */
		msg_send(&timer_msg, timer_pid);
    }
    
    if (strcmp(cmd, "period") == 0) {
        char *val = argv[2];
        set_period(atoi(val));
    }
    
    if (strcmp(cmd, "reset") == 0) {
        reset_config();
        save_config();
    }
    
    return 1;
}

void umdk_lmt01_init(uwnds_cb_t *event_callback) {

	callback = event_callback;
	init_config();
    printf("[umdk-" _UMDK_NAME_ "] Publish period: %d min\n", lmt01_config.publish_period_min);

	init_sensors();

	/* Create handler thread */
	char *stack = (char *) allocate_stack(UMDK_LMT01_STACK_SIZE);
	if (!stack) {
		return;
	}

    unwds_add_shell_command(_UMDK_NAME_, "type '" _UMDK_NAME_ "' for commands list", umdk_lmt01_shell_cmd);
    
	timer_pid = thread_create(stack, UMDK_LMT01_STACK_SIZE, THREAD_PRIORITY_MAIN - 1, THREAD_CREATE_STACKTEST, timer_thread, NULL, "lmt01 thread");

    /* Start publishing timer */
	lptimer_set_msg(&timer, 60000 * lmt01_config.publish_period_min, &timer_msg, timer_pid);
}

static void reply_fail(module_data_t *reply) {
	reply->length = 2;
	reply->data[0] = _UMDK_MID_;
	reply->data[1] = 255;
}

static void reply_ok(module_data_t *reply) {
	reply->length = 2;
	reply->data[0] = _UMDK_MID_;
	reply->data[1] = 0;
}

bool umdk_lmt01_cmd(module_data_t *cmd, module_data_t *reply) {
	if (cmd->length < 1) {
		reply_fail(reply);
		return true;
	}

	umdk_lmt01_cmd_t c = cmd->data[0];
	switch (c) {
	case UMDK_LMT01_CMD_SET_PERIOD: {
		if (cmd->length != 2) {
			reply_fail(reply);
			break;
		}

		uint8_t period = cmd->data[1];
		set_period(period);

		reply_ok(reply);
		break;
	}

	case UMDK_LMT01_CMD_POLL:
		is_polled = true;

		/* Send signal to publisher thread */
		msg_send(&timer_msg, timer_pid);

		return false; /* Don't reply now */

		break;

	case UMDK_LMT01_CMD_SET_GPIOS: {
		uint8_t *gpios = &cmd->data[1];
		int num_gpios = cmd->length - 1;

		if (!num_gpios) {
			reply_fail(reply);
			break;
		}

		int i;
		for (i = 0; i < num_gpios; i++) {
			if (gpios[i]) {
				gpio_t gpio = unwds_gpio_pin(gpios[i]);
				en_pins[i] = gpio;
			} else
				en_pins[i] = 0;	/* Disable this pin */
		}

		/* Re-initialize sensors */
		init_sensors();

		reply_ok(reply);
		break;
	}

	default:
		reply_ok(reply);
		break;
	}

	return true;
}

